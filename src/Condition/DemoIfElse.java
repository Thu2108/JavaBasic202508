package Condition;

public class DemoIfElse {
    public static void main(String[] args) {

        int age = 17;
        String gender = "Nữ";
        if ((age >= 18) &&  gender.equals("Nữ")) {
            System.out.println("Nữ đủ tuổi kết hôn");

        } else {
            System.out.println("Nữ chưa đủ tuổi kết hôn");
        }

    }
}
